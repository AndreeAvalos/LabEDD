package org.usac.edd;

/**
 *
 * @author esvux
 */
public class Arista {
    private String destino;
    private double peso;

    public Arista() {
    }

    public String getDestino() {
        return destino;
    }

    public void setDestino(String destino) {
        this.destino = destino;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }
    
}
